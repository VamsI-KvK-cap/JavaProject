package com.example.reclaim;

public class ReclaimMasterData {
    private String reclaimMaster;
    private String userRoad;
    private String reclaimCode;
    private String extendedReclaimCode;
    private String effectiveMonth;
    private String expireMonth;

    public String getReclaimMaster() {
        return reclaimMaster;
    }

    public void setReclaimMaster(String reclaimMaster) {
        this.reclaimMaster = reclaimMaster;
    }

    public String getUserRoad() {
        return userRoad;
    }

    public void setUserRoad(String userRoad) {
        this.userRoad = userRoad;
    }

    public String getReclaimCode() {
        return reclaimCode;
    }

    public void setReclaimCode(String reclaimCode) {
        this.reclaimCode = reclaimCode;
    }

    public String getExtendedReclaimCode() {
        return extendedReclaimCode;
    }

    public void setExtendedReclaimCode(String extendedReclaimCode) {
        this.extendedReclaimCode = extendedReclaimCode;
    }

    public String getEffectiveMonth() {
        return effectiveMonth;
    }

    public void setEffectiveMonth(String effectiveMonth) {
        this.effectiveMonth = effectiveMonth;
    }

    public String getExpireMonth() {
        return expireMonth;
    }

    public void setExpireMonth(String expireMonth) {
        this.expireMonth = expireMonth;
    }
}